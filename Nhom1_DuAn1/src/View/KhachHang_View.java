    private void txtTimKHKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKHKeyPressed
        // TODO add your handling code here:
        String TenKH = txtTimKH.getText();
               listKH = KHrepo.timTen(TenKH);
               if(listKH.size()<1){
                   listKH=KHrepo.timMa(TenKH);
               }
        int stt = tblKhachHang.getSelectedRow();
        stt += 1;

        model.setRowCount(0);
        for (KhachHang kh : listKH) {
            String gioiTinh = kh.getGioiTinh() == 1 ? "Nam" : "Nữ";
            model.addRow(new Object[]{
                stt += 1,
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getEmail(),
                kh.getDiaChi(),
                kh.getSDT(),
                gioiTinh,});

        }
    }//GEN-LAST:event_txtTimKHKeyPressed


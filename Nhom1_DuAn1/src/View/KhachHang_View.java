    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        List<KhachHang> list = new ArrayList();
        if (Validate()) {
            int chon = JOptionPane.showConfirmDialog(this, "Bạn Có Muốn Thêm Không ? Y/N", "Thông Báo", JOptionPane.YES_NO_OPTION);
            if (chon == JOptionPane.YES_OPTION) {
                String MaKH = txtMaKH.getText();
                String TenKH = txtTenKH.getText();
                String SDT = txtSDT.getText();
                String Email = txtEmail.getText();
                String DiaChi = txtDiaChi.getText();
                int GioiTinh;
                if (rdNam.isSelected()) {
                    GioiTinh = 1;
                } else {
                    GioiTinh = 0;
                }
                KhachHang kh = new KhachHang();
                kh.setMaKH(MaKH);
                kh.setTenKH(TenKH);
                kh.setEmail(Email);
                kh.setDiaChi(DiaChi);
                kh.setSDT(SDT);
                kh.setGioiTinh(GioiTinh);
                boolean addKH = KHrepo.inertKH(kh);
                list = KHrepo.getAll();
                fillTable();
                if (addKH) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ban chon thoat");
            }

        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tblKhachHang.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Mời Chọn Khách Hàng Cần Xóa");
        } else {
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa Y/N", "Thông Báo", JOptionPane.YES_NO_OPTION);
            if (chon == JOptionPane.YES_OPTION) {
                String ma = tblKhachHang.getValueAt(index, 1).toString();
                if (KHrepo.deleteKH(ma) > 0) {
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    fillTable();

                } else {
                    JOptionPane.showMessageDialog(this, "Xóa that bai");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Bạn Chọn thoat");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed


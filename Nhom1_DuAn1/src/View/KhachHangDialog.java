    public void loadTableKhachHang(List<KhachHang> lst) {
        int i = 0;
        tblModelKhachHang.setRowCount(0);
        for (KhachHang kh : lst) {
            tblModelKhachHang.addRow(new Object[]{
                ++i,
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getGioiTinh() == 0 ? "Nữ" : "Nam",
                kh.getSDT(),
                kh.getEmail(),
                kh.getDiaChi()
            });
        }

    }

    public void clickKhachHang() {
        int index = tblKhachHang.getSelectedRow();
        this.khachHang = this.lstKhachHang.get(index);
        int Rowindex = tblKhachHang.getSelectedRow();
        KhachHang kh = KHrepo.getAll().get(Rowindex);
        txtMaKH.setText(kh.getMaKH());
        txtTenKH.setText(kh.getTenKH());
        if (kh.getGioiTinh() == 1) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtEmail.setText(kh.getEmail());
        txtSDT.setText(kh.getSDT());
        txtDiaChi.setText(kh.getDiaChi());

    }

    public void chonKhachHang() {
        int index = tblKhachHang.getSelectedRow();
        this.khachHang = this.lstKhachHang.get(index);
        if (this.khachHang == null) {
            JOptionPane.showMessageDialog(this, "Vui Lòng Chọn Khách Hàng");
        }
        this.dispose();
    }

    boolean Validate() {

        if (txtTenKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không bỏ trống");

            return false;
        }
        if (txtMaKH.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không bỏ trống");

            return false;
        }
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email không để trống");
            return false;
        } else if (txtEmail.getText().contains("a-zA-Z0-9" + "." + "@" + "a-zA-Z0-9")) {
            JOptionPane.showMessageDialog(this, "Email không đúng định dạng");
            return false;
        }
        if (txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Điện thoại không để trống");
        } else {
            try {
                int phone;
                phone = Integer.parseInt(txtSDT.getText());
                if (phone < 10) {
                    JOptionPane.showMessageDialog(this, "Số đt không quá 10 số");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không đúng định dạng");
            }
        }
        if (txtDiaChi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không để trống");
            return false;
        }
        return true;
    }


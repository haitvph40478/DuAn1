    private void txtTimKHKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKHKeyPressed
        // TODO add your handling code here:
        String TenKH = txtTimKH.getText();
        lstKhachHang = KHrepo.timTen(TenKH);
        if(lstKhachHang.size()<1){
            lstKhachHang=KHrepo.timMa(TenKH);
        }
        int stt = tblKhachHang.getSelectedRow();
        stt += 1;

        tblModelKhachHang.setRowCount(0);
        for (KhachHang kh : lstKhachHang) {
            String gioiTinh = kh.getGioiTinh() == 1 ? "Nam" : "Nữ";
            tblModelKhachHang.addRow(new Object[]{
                stt += 1,
                kh.getMaKH(),
                kh.getTenKH(),
                kh.getEmail(),
                kh.getDiaChi(),
                kh.getSDT(),
                gioiTinh,});

        }
    }//GEN-LAST:event_txtTimKHKeyPressed


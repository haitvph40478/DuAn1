    void fillTable_QuaiDeo(List<QuaiDeo> list) {
        model = (DefaultTableModel) tbQuaiDeo.getModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("STT");
        model.addColumn("Mã Quai Đeo");
        model.addColumn("Tên Quai Đeo");

        int stt = tbQuaiDeo.getSelectedRow();
        stt = stt + 1;
        for (QuaiDeo cl : list) {
            Object[] row = new Object[]{
                stt = stt + 1,
                cl.getMaQuaiDeo(),
                cl.getTenQuaiDeo()};
            model.addRow(row);
        }
    }

    QuaiDeo readForm_QD() {
        String maSN = txtmaQD.getText();
        String tenSN = txttenQD.getText();

        QuaiDeo v = new QuaiDeo();
        v.setMaQuaiDeo(maSN);
        v.setTenQuaiDeo(tenSN);

        return v;
    }

    void fillTable_SoNgan(List<SoNgan> list) {
        model = (DefaultTableModel) tbSoNgan.getModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("STT");
        model.addColumn("Mã số ngăn");
        model.addColumn("Tên số ngăn");

        int stt = tbSoNgan.getSelectedRow();
        stt = stt + 1;
        for (SoNgan cl : list) {
            Object[] row = new Object[]{
                stt = stt + 1,
                cl.getMaSoNgan(),
                cl.getTenSoNgan()};
            model.addRow(row);
        }
    }

    SoNgan readForm_SN() {
        String maSN = txtmaSN.getText();
        String tenSN = txttenSN.getText();

        SoNgan v = new SoNgan();
        v.setMaSoNgan(maSN);
        v.setTenSoNgan(tenSN);

        return v;
    }


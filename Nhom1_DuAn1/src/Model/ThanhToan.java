package Model;

/**
 *
 * @author Admin
 */
public class ThanhToan {
    private Integer id;
    private String ma,ten,moTa;

    public ThanhToan() {
    }

    public ThanhToan(Integer id, String ma, String ten, String moTa) {
        this.id = id;
        this.ma = ma;
        this.ten = ten;
        this.moTa = moTa;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getMa() {
        return ma;
    }

    public void setMa(String ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
    
    
    
}
